"""
Обучающая программа: Инъекционные атаки (SQL Injection & XSS)
Демонстрирует уязвимости и способы защиты.
"""

import html  # Для экранирования HTML

# ------------------- Эмуляция базы данных -------------------
# Простая эмуляция базы данных в виде словаря
USER_DB = {
    "alice": {"name": "Alice", "role": "user", "password": "pass123"},
    "bob": {"name": "Bob", "role": "admin", "password": "admin456"},
    "eve": {"name": "Eve", "role": "user", "password": "eve789"},
}


def emulate_db_query(sql_query):
    """
    Эмуляция выполнения SQL-запроса (в реальной БД было бы опасно!).
    Здесь мы просто извлекаем логику поиска пользователя по ID.
    """
    print(f"\n[Эмуляция БД] Выполняется запрос: {sql_query}")

    # Эмулируем уязвимый поиск: если в запросе есть ' OR '1'='1
    if "' OR '1'='1" in sql_query or "1=1" in sql_query:
        print("[ЭМУЛЯЦИЯ УЯЗВИМОСТИ] Все записи извлечены!")
        return list(USER_DB.keys())
    elif "DROP TABLE" in sql_query.upper():
        print("[ЭМУЛЯЦИЯ УЯЗВИМОСТИ] Таблица users была бы удалена!")
        return []
    else:
        # Простой поиск по имени пользователя
        for user_id in USER_DB:
            if user_id in sql_query:
                return [user_id]
        return []


# ------------------- 1. SQL Injection (уязвимый код) -------------------
def vulnerable_sql_login():
    print("\n" + "=" * 60)
    print("1. УЯЗВИМЫЙ SQL-ЗАПРОС (SQL Injection)")
    print("=" * 60)

    user_input = input("Введите имя пользователя для поиска: ")

    # ОПАСНО! Прямая подстановка строки в SQL-запрос
    sql_query = f"SELECT * FROM users WHERE username = '{user_input}'"
    result = emulate_db_query(sql_query)

    if result:
        print(f"Найденные пользователи: {result}")
        if len(result) > 1:
            print("[!!!] АТАКА SQL Injection УСПЕШНА! Хакер получил всех пользователей!")
    else:
        print("Пользователь не найден.")


# ------------------- 2. SQL Injection (защищённый код) -------------------
def safe_sql_login():
    print("\n" + "=" * 60)
    print("2. ЗАЩИЩЁННЫЙ SQL-ЗАПРОС (параметризация)")
    print("=" * 60)

    user_input = input("Введите имя пользователя для поиска: ")

    # Безопасно: параметризация (здесь эмуляция через разрешённые символы)
    allowed_chars = set("abcdefghijklmnopqrstuvwxyzABCDEFGHIJKLMNOPQRSTUVWXYZ0123456789_")
    safe_input = "".join(ch for ch in user_input if ch in allowed_chars)

    print(f"[Безопасно] Отфильтрованный ввод: {safe_input}")

    sql_query = f"SELECT * FROM users WHERE username = '{safe_input}'"
    result = emulate_db_query(sql_query)

    if result and len(result) == 1:
        print(f"Пользователь найден: {result[0]}")
    elif result and len(result) > 1:
        print("[ЗАЩИТА СРАБОТАЛА] Несколько записей не возвращено, атака не удалась!")
    else:
        print("Пользователь не найден.")


# ------------------- 3. XSS (межсайтовый скриптинг) -------------------
def vulnerable_xss_comment_system():
    print("\n" + "=" * 60)
    print("3. УЯЗВИМЫЙ КОММЕНТАРИЙ (XSS - отражающий/хранимый)")
    print("=" * 60)

    comments = []

    print("Эмуляция форума: добавьте комментарий")
    user_comment = input("Ваш комментарий: ")

    # ОПАСНО! Сохраняем как есть
    comments.append(user_comment)

    print("\n--- Все комментарии (НЕ БЕЗОПАСНЫЙ ВЫВОД) ---")
    for comment in comments:
        # Выводим напрямую — если в комментарии <script>, он "выполнился" бы в браузере
        print(f"Комментарий: {comment}")

    print("\n[!!!] Если вы ввели <script>alert('XSS')</script> — в реальном браузере это бы выполнилось!")


def safe_xss_comment_system():
    print("\n" + "=" * 60)
    print("4. ЗАЩИЩЁННЫЙ КОММЕНТАРИЙ (экранирование HTML)")
    print("=" * 60)

    comments = []

    print("Эмуляция форума: добавьте комментарий")
    user_comment = input("Ваш комментарий: ")

    # Безопасно! Экранируем HTML-теги
    safe_comment = html.escape(user_comment)
    comments.append(safe_comment)

    print("\n--- Все комментарии (БЕЗОПАСНЫЙ ВЫВОД) ---")
    for comment in comments:
        print(f"Комментарий: {comment}")

    print(
        "\n[ЗАЩИТА] Вредоносный <script> превратился в &lt;script&gt; и не выполнится в браузере."
    )


# ------------------- 4. Дополнительная демо: инъекция через ввод -------------------
def demo_injection_awareness():
    print("\n" + "=" * 60)
    print("5. ПОЧЕМУ ИНЪЕКЦИИ ОПАСНЫ (даже если не работает полностью)")
    print("=" * 60)

    print(
        "Злоумышленник может ввести: ' OR 1=1; -- \n"
        "Или: '; DROP TABLE users; --\n"
        "Или: <img src=x onerror=alert('XSS')>\n"
    )
    test_input = input("Попробуйте ввести вредоносную строку: ")

    print("\n[Реакция уязвимой системы]:")
    # Эмуляция ошибки SQL, которая даёт информацию хакеру
    if "'" in test_input:
        print("SQL Error: syntax error near '''' — хакер узнал, что используется SQL-синтаксис с кавычками.")
    if "--" in test_input:
        print("SQL Error: unexpected end of comment — хакер понял, что комментарии работают.")
    if "DROP" in test_input.upper():
        print("Потенциально опасная команда DROP — система может упасть или выдать детали структуры таблиц.")

    print("\n[Вывод]: Даже неудачная атака даёт хакеру информацию о системе.")


# ------------------- Меню -------------------
def main():
    while True:
        print("\n" + "=" * 60)
        print("🔐 ИНЪЕКЦИОННЫЕ АТАКИ — ДЕМОНСТРАЦИЯ")
        print("1. SQL Injection (уязвимый код)")
        print("2. SQL Injection (защищённый код — параметризация)")
        print("3. XSS — межсайтовый скриптинг (уязвимый комментарий)")
        print("4. XSS — защищённый комментарий (экранирование)")
        print("5. Как даже неудачная инъекция выдаёт информацию")
        print("0. Выход")
        print("=" * 60)

        choice = input("Выберите опцию: ")

        if choice == "1":
            vulnerable_sql_login()
        elif choice == "2":
            safe_sql_login()
        elif choice == "3":
            vulnerable_xss_comment_system()
        elif choice == "4":
            safe_xss_comment_system()
        elif choice == "5":
            demo_injection_awareness()
        elif choice == "0":
            print("Выход из программы.")
            break
        else:
            print("Неверный ввод. Попробуйте снова.")


if __name__ == "__main__":
    main()