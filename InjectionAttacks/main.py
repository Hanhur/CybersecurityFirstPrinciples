# Инъекционные атаки:Инъекционные атаки - это атаки, при которых
# система ожидает какого-либо ввода от пользователя, но вместо отправки
# такого ввода злоумышленник отправляет вредоносный материал, такой
# как код, который принимающая система затем либо выполняет, либо
# распространяет другим для выполнения. Несмотря на то, что правильное
# кодирование приложений может, по крайней мере теоретически,
# предотвратить большинство форм инъекционных атак, реальность такова,
# что многие (если не большинство) систем остаются уязвимыми для таких
# атак, и в результате инъекционные атаки являются чрезвычайно часто
# используемым инструментом в арсенале хакеров. Межсайтовый скриптинг: Межсайтовый скриптинг (XSS) - это
# особый тип инъекционной атаки, при которой злоумышленник
# добавляет вредоносный код на законный веб-сайт, так что, когда
# пользователь посещает соответствующий веб-сайт (через веббраузер или приложение), вредоносный код доставляется на
# устройство пользователя и выполняется там. Злоумышленник
# может вставить вредоносный код на легитимный сервер, поскольку сервер позволяет пользователям отправлять материалы, которые
# затем будут показаны другим пользователям.
# Онлайн-форумы пользователей и платформы социальных сетей
# являются основными кандидатами на атаки с использованием
# межсайтовых скриптов, если они должным образом не защищены
# от таких атак. То же самое относится и к веб-сайтам, которые
# позволяют пользователям комментировать информацию, такую как
# новостная статья. Например, XSS-атака может произойти, если
# хакер отправляет вредоносный код в комментарии таким образом,
# что когда браузер последующего пользователя попытается
# отобразить комментарий, он в конечном итоге выполнит код. Внедрение SQL: Атаки с использованием SQL-инъекций
# представляют собой особый тип инъекционных атак, которые
# используют способ хранения данных большинством компьютерных
# систем, который находится в реляционных базах данных, которые
# обеспечивают доступ к людям и системам с помощью того, что
# известно как интерфейсы стандартного языка структурированных
# запросов (SQL). Когда злоумышленник запускает атаку с
# использованием SQL-инъекций, он просто отправляет в систему
# данные, которые включают SQL-команды, а не обычные данные.
# Например, если система просит пользователя ввести
# идентификатор пользователя для поиска по нему, а
# злоумышленник, зная о команде SQL, которая, вероятно, будет
# использоваться системой в своей базе данных для выполнения
# этого поиска, вместо этого отправляет идентификатор
# пользователя, который состоит из кода как для выполнения этой
# команды, так и для выдачи другой команды для отображения всех
# записей в базе данных, система, если она не защищена от
# внедрения SQL, может сделать именно то, что хочет
# злоумышленник. Даже если атака с использованием SQL-инъекции не работает
# полностью — и атакуемая система не отображает данные — ответ
# системы на атаку с использованием SQL-инъекции все равно может
# раскрыть информацию о том, как она обрабатывает SQL-инъекцию,
# тем самым предоставляя хакеру информацию о системе, базе
# данных и действующих механизмах безопасности (или
# информацию о том, чего нет, но что должно быть).

