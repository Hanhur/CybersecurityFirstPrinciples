# Стандарт безопасности данных индустрии платежных карт (PCI DSS) -
# это стандарт информационной безопасности для организаций, которые
# обрабатывают основные кредитные карты и связанную с ними информацию.
# Стандарт неоднократно обновлялся и расширялся; самой последней версией
# является версия 3.2.1, опубликованная в мае 2018 года.
# Хотя все компании любого размера, на которые распространяется стандарт
# PCI DSS, должны соответствовать ему, PCI учитывает различные уровни
# ресурсов, доступных предприятиям разного размера. Соответствие
# требованиям PCI фактически имеет четыре различных уровня. Уровень,
# которому должна соответствовать организация, обычно зависит в первую
# очередь от того, сколько транзакций по кредитным картам она обрабатывает
# в год. Другие факторы, такие как степень риска платежей, которые получает
# компания, также имеют значение. Различные уровни 

# Уровень PCI 4: Стандарты для предприятий, которые обрабатывают
# менее 20 000 транзакций по кредитным картам в год

# Уровень PCI 3: Стандарты для предприятий, обрабатывающих от 20 000
# до 1 000 000 транзакций по кредитным картам в год

# Уровень PCI 2: Стандарты для предприятий, обрабатывающих от 1 000
# 000 до 6 000 000 транзакций по кредитным картам в год

# Уровень PCI 1: Стандарты для предприятий, обрабатывающих более 6
# 000 000 транзакций по кредитным картам в год

# Подробное изучение PCI выходит за рамки данной книги. По этой теме
# написаны целые книги, и различные организации предлагают занятия,
# посвященные этой теме. Если вы ведете небольшой бизнес и обрабатываете
# платежи по кредитным картам или храните данные кредитной карты по
# любой другой причине, обязательно обратитесь к специалисту,
# разбирающемуся в PCI, который поможет вам сориентироваться. Во многих
# случаях обработчики ваших кредитных карт смогут порекомендовать вам
# подходящего консультанта или провести вас самостоятельно.

# ===================================================================================================================================================

def determine_pci_level(transactions: int) -> dict:
    """
    Определяет уровень PCI DSS на основе количества транзакций по кредитным картам в год.
    
    Аргументы:
        transactions (int): количество транзакций за год
    
    Возвращает:
        dict: словарь с уровнем, диапазоном и описанием
    """
    if transactions < 0:
        raise ValueError("Количество транзакций не может быть отрицательным")
    
    if transactions < 20000:
        level = 4
        range_desc = "менее 20 000 транзакций"
    elif transactions < 1_000_000:
        level = 3
        range_desc = "от 20 000 до 1 000 000 транзакций"
    elif transactions < 6_000_000:
        level = 2
        range_desc = "от 1 000 000 до 6 000 000 транзакций"
    else:
        level = 1
        range_desc = "более 6 000 000 транзакций"
    
    return {
        "level": level,
        "range": range_desc,
        "transactions": transactions
    }


def print_pci_report(transactions: int) -> None:
    """Выводит подробный отчёт об уровне PCI DSS."""
    result = determine_pci_level(transactions)
    
    print("=" * 60)
    print("ОПРЕДЕЛЕНИЕ УРОВНЯ PCI DSS")
    print("=" * 60)
    print(f"Годовое количество транзакций: {result['transactions']:,}")
    print(f"Уровень PCI: {result['level']}")
    print(f"Диапазон: {result['range']}")
    print("-" * 60)
    
    # Дополнительная информация из текста
    print("\n📌 КОММЕНТАРИЙ:")
    if result['level'] == 4:
        print("- Ваш бизнес обрабатывает менее 20 000 транзакций в год.")
        print("- Это наименьший уровень требований, но соответствие всё равно обязательно.")
    elif result['level'] == 3:
        print("- Ваш бизнес обрабатывает от 20 000 до 1 млн транзакций в год.")
        print("- Требования выше, чем для 4 уровня.")
    elif result['level'] == 2:
        print("- Ваш бизнес обрабатывает от 1 до 6 млн транзакций в год.")
        print("- Это высокий уровень — необходима серьёзная система безопасности.")
    else:
        print("- Ваш бизнес обрабатывает более 6 млн транзакций в год.")
        print("- Это максимальный уровень PCI (Level 1).")
        print("- Требуется ежегодный аудит и тестирование на проникновение.")
    
    print("\n⚠️  ВАЖНО:")
    print("- Стандарт PCI DSS обязателен для всех компаний, работающих с картами.")
    print("- Последняя версия на момент текста: 3.2.1 (май 2018).")
    print("- Для малого бизнеса настоятельно рекомендуется обратиться к специалисту.")
    print("- Обработчики кредитных карт часто помогают с прохождением PCI.")
    print("=" * 60)


def interactive_mode():
    """Интерактивный режим программы."""
    print("\n🔐 Калькулятор уровня PCI DSS")
    print("Программа определяет уровень стандарта безопасности данных индустрии платежных карт.\n")
    
    while True:
        try:
            user_input = input("Введите количество транзакций за год (или 'выход' для завершения): ")
            
            if user_input.lower() in ['выход', 'exit', 'quit', 'q']:
                print("Выход из программы. До свидания!")
                break
            
            transactions = int(user_input.replace(" ", "").replace(",", ""))
            print_pci_report(transactions)
            print()
            
        except ValueError:
            print("❌ Ошибка: введите целое положительное число или 'выход'.")
        except KeyboardInterrupt:
            print("\n\nПрограмма прервана пользователем.")
            break


if __name__ == "__main__":
    # Примеры использования
    print("Примеры определения уровня PCI:")
    test_values = [5000, 100_000, 3_000_000, 10_000_000]
    
    for val in test_values:
        info = determine_pci_level(val)
        print(f"{val:>12,} транзакций → Уровень PCI {info['level']} ({info['range']})")
    
    print("\n" + "─" * 60)
    
    # Запуск интерактивного режима
    interactive_mode()